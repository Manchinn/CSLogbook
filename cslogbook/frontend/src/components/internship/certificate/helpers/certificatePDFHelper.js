        studentId: cleanThaiText(studentInfo.studentId || ''),
        fullName: cleanThaiText(studentInfo.fullName || ''),
        firstName: cleanThaiText(studentInfo.firstName || ''),
        lastName: cleanThaiText(studentInfo.lastName || ''),
